# 문제 해석
# 브루트 포스 알고리즘
# 완전탐색 알고리즘. 즉, 가능한 모든 경우의 수를 모두 탐색하면서 요구조건에 충족되는 결과만을 가져온다.
# 해가 하나 이상 존재한다는 가정을 세우고 모든 범위를 탐색하기 때문에 무조건 정답을 찾을 수 있다.

# 문제 해결 방법
# 1. 주어진 문제를 선형 구조로 구조화 한다.
# 2. 구조화된 문제공간을 적절한 방법으로 해를 구성할 때까지 탐색한다.
# 3. 구성된 해를 정리한다.

